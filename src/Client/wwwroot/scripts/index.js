const openMenu = () => {
    const menu = document.querySelector(".navigation-options");
    menu.style.display = menu.style.display == "flex" ? "none" : "flex";
};


const OpenRolepicker = () => {
    const rolepicker = document.querySelector("#rolepicker");
    rolepicker.style.display = rolepicker.style.display == "flex" ? "none" : "flex";
}
